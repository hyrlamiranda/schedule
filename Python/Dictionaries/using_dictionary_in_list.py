### Using a dictionary in a list

book_0 = {'name' : 'elon musk', 'author': 'ashlee vance', 'price': '15.00', 'publisher': 'virgin book'}
book_1 = {'name' : 'The Light Between Oceans', 'author': 'M.L. Stedman', 'price': '15.00', 'publisher': 'Random House Australia'}
book_2 = {'name' : 'The Nightingale', 'author': 'Kristin Hannah', 'price': '15.00', 'publisher' : 'none'}

books = [book_0, book_1, book_2]

for book in books:
	print(book)